M = magic(300);
[nrow, ncol] = size(M);
found = 0;

for i = 1:nrow
    for j = 1:ncol
        if (M(i,j) == 19)
            disp(['row: ', num2str(i)])
            disp(['column: ', num2str(j)])
            found = 1;
            break
        end
    end

    if (found == 1)
        break
    end
end


